class MathEx
{
	public static void main(String[] args) {
	

        System.out.println("abs(25.5) = " + Math.abs(25.5));
        System.out.println("ceil(25.5) = " + Math.ceil(25.5));
        System.out.println("floor(25.5) = " + Math.floor(25.5));
        System.out.println("max(25.5, 12.3) = " + Math.max(25.5, 12.3));
        System.out.println("min(25.5, 12.3) = " + Math.min(25.5, 12.3));
        System.out.println("pow(25.5, 12.3) = " + Math.pow(25.5, 12.3));
        System.out.println("sqrt(25.5) = " + Math.sqrt(25.5));
        System.out.println("round(25.5) = " + Math.round(25.5));
        System.out.println("log(10) = "+Math.log(10));
        System.out.println("random() = " + Math.random());

	}
}